# Voigt function by power law expansion in a, a >= 0.0
# Taken from Harris, 1948, ApJ, 108, 112
# Relative error with respect to voigt_de is < 10^-4.9 for a < 0.1,
# gets worse for larger a (since it's an expansion in a)
# Doesn't test for a == 0.0 because it's rare and it'll get it right anyway


def voigtslow(a, u):
    """ Calculate the voigt function H(a,u).

    Uses numerical integration, so is slow.  Answer is correct to 20
    significant figures.
    """
    with mp.workdps(20):
        z = mp.mpc(u, a)
        result = mp.exp(-z*z) * mp.erfc(-1j*z)

    return result.real

def voigt(a, u):
    """ Compute the Voigt function.

    a: float
      The lorentzian FWHM divided by the gaussian FWHM of the line.
    u: array of floats
      The frequency offsets from the frequency at the line centre, in
      units of the FWHM frequency of the doppler broadening.

    Returns voigt function H(a,u).

    Notes
    -----
    This is a Taylor approximation to the Voigt function valid for 0 < a <
    0.1. (Harris 1948, ApJ, 108, 112).  Relative error with respect to
    voigt_de is < 10^-4.9 for a < 0.1, gets worse for larger a (since
    it's an expansion in a).

    Examples
    --------
    >>> uvals = np.arange(-25, 25, 1./100)
    >>> a = 0.01
    >>> y1 = voigt(a, uvals)
    >>> y2 = np.fromfile('testdata/idl_voigt.txt', sep=' ')
    >>> np.allclose(y1, y2, rtol=1e-8)
    True
    >>> uvals = np.linspace(-25, 25, 40)
    >>> a = 1e-5
    >>> y1 = voigt(a, uvals)
    >>> y2 = [voigtslow(a,u) for u in uvals]
    >>> all(mp.almosteq(i, j, 1e-5) for i,j in zip(y1,y2))
    True
    >>> a = 0.1
    >>> y1 = voigt(a, uvals)
    >>> y2 = [voigtslow(a,u) for u in uvals]
    >>> all(mp.almosteq(i, j, 1e-5) for i,j, in zip(y1,y2))
    True
    """
    a = float(a)
    if not 0 <= a <= 0.1:
        raise ValueError('a must satisfy 0 <= a <= 0.1 (%f)' % a)
    u = np.abs(u)
    out = np.empty_like(u)
    u2 = u*u

    cond = u > 19.99

    if np.any(cond):
        # Use asymptotic approximation.
        u2c = u2[cond]
        u2c2 = u2c * u2c
        out[cond] = a / (sqrtpi * u2c) * (
            1. + 1.5/u2c + 3.75/u2c2 - a*a/u2c * (1. + 5./u2c + 26.25/u2c2) )

    # for u values with abs(u) <= 19.99 use lookup tables
    notcond = ~cond
    u = u[notcond]
    u2 = u2[notcond]
    expmu2 = np.exp(-u2)

    out[notcond] = expmu2 + a*(np.interp(u, uvals, H1) + a*(
        (1. - 2.*u2)*expmu2  + a*(np.interp(u, uvals, H3) + a*(
        0.5 - 2.*u2 + 2./3.*u2*u2)*expmu2)))
    return out

        
